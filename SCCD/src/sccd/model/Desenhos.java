package sccd.model;

/**
 *
 * @author mrs_a
 */
public class Desenhos {
    
    // Atributos    
    private int id;
    private int faca;
    private float comprimento;
    private float largura;
    private float altura;
    private String colagem;
    private String abas;
    private String berco;
    private String promocional;
    private String operador;
    private String datahora;
    
    //Getters e Setters

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getFaca() {
        return faca;
    }

    public void setFaca(int faca) {
        this.faca = faca;
    }

    public float getComprimento() {
        return comprimento;
    }

    public void setComprimento(float comprimento) {
        this.comprimento = comprimento;
    }

    public float getLargura() {
        return largura;
    }

    public void setLargura(float largura) {
        this.largura = largura;
    }

    public float getAltura() {
        return altura;
    }

    public void setAltura(float altura) {
        this.altura = altura;
    }

    public String getColagem() {
        return colagem;
    }

    public void setColagem(String colagem) {
        this.colagem = colagem;
    }

    public String getAbas() {
        return abas;
    }

    public void setAbas(String abas) {
        this.abas = abas;
    }

    public String getBerco() {
        return berco;
    }

    public void setBerco(String berco) {
        this.berco = berco;
    }

    public String getPromocional() {
        return promocional;
    }

    public void setPromocional(String promocional) {
        this.promocional = promocional;
    }

    public String getOperador() {
        return operador;
    }

    public void setOperador(String operador) {
        this.operador = operador;
    }

    public String getDatahora() {
        return datahora;
    }

    public void setDatahora(String datahora) {
        this.datahora = datahora;
    }
    
}
